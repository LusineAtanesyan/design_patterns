package com.patterns.facade.sound;

public class VolumeLevel {

    public void load() {
        System.out.println("The volume level is load");
    }

    public void medium() {
        System.out.println("The volume level is medium");
    }

    public void soft() {
        System.out.println("The volume level is soft");
    }

    public void off() {
        System.out.println("The volume level is off");
    }

}
