package com.patterns.facade.light;

public class LightLevel {

    public void bright() {
        System.out.println("The light level is bright");
    }

    public void medium() {
        System.out.println("The light level is medium");
    }

    public void dark() {
        System.out.println("The light level is dark");
    }

    public void off() {
        System.out.println("The light level is off");
    }
}
